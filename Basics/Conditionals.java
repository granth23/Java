package Basics;
public class Conditionals {
    public static void main(String[] args) {
        int a = 3;
        if (a > 5) {
            System.out.println("More");
        }
        else if (a == 5) {
            System.out.println("Equal");
        }
        else {
            System.out.println("Less");
        }
    }
}
